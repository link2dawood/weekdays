// Finnish name days (nimipäivät) — D-02.
//
// ⚠️ PLACEHOLDER DATA — DO NOT SHIP AS-IS. The real calendar must come from
// the Helsinki University Almanac Office (Almanakkatoimisto), which holds a
// specific statutory exclusive right to publish almanacs in Finland and
// asserts rights over their compiled nimipäiväkalenteri. Per the
// engineering handoff: "confirm terms rather than scraping" — this is a
// blocking legal question (see HANDOFF.md, "Open questions", item 1), not a
// technical one, and it has not been resolved yet.
//
// This file exists only so the data shape, versioning metadata, and
// nameDaysForWeek() API are ready to receive the real calendar the moment
// licensing is confirmed. Every name below is a synthetic placeholder
// encoding its own date (e.g. "PLACEHOLDER-01-15") — not sourced from the
// Almanac Office, Wikipedia, or anywhere else — specifically so it's
// impossible to mistake for real data if it ever rendered anywhere.

import { mondayOf } from "../components/dateUtils";

// Documents which edition of the calendar this data reflects, since the
// real one "is revised periodically" (handoff) and that has to be tracked.
// All null until real, licensed data replaces the placeholder below.
export const CALENDAR_META = {
  language: "fi",
  source: null, // e.g. "Helsingin yliopiston almanakkatoimisto"
  edition: null, // e.g. the almanac's stated revision year/range
  licensedOn: null, // date the licensing terms were confirmed
  isPlaceholder: true,
};

// Swedish-language (Finland) calendar is optional per the handoff, and
// scaffolded identically — it carries the same licensing question, not a
// separate one.
export const CALENDAR_META_SV = {
  language: "sv-FI",
  source: null,
  edition: null,
  licensedOn: null,
  isPlaceholder: true,
};

// Keyed by "MM-DD" (calendar date), not a day-of-year ordinal: ordinal
// numbering shifts after February in leap vs. non-leap years (e.g. 1 March
// is day 61 in a leap year, day 60 otherwise), which is exactly the kind of
// off-by-one this data is prone to. MM-DD sidesteps that entirely.
//
// Generated by walking a leap year (2024) day by day so all 366 possible
// calendar dates get a key, including "02-29" — Date arithmetic elsewhere
// in the app simply never produces a 29 February date object in a
// non-leap-year context, so no special-casing is needed when *looking up* a
// date; the key just needs to exist for when it does.
function buildPlaceholderCalendar() {
  const map = {};
  const start = new Date(2024, 0, 1);
  for (let i = 0; i < 366; i++) {
    const d = new Date(start);
    d.setDate(start.getDate() + i);
    const mm = String(d.getMonth() + 1).padStart(2, "0");
    const dd = String(d.getDate()).padStart(2, "0");
    map[`${mm}-${dd}`] = [`PLACEHOLDER-${mm}-${dd}`];
  }
  return map;
}

export const NAME_DAYS_FI = buildPlaceholderCalendar();
export const NAME_DAYS_SV = buildPlaceholderCalendar();

export function nameDaysForDate(date, calendar = NAME_DAYS_FI) {
  const mm = String(date.getMonth() + 1).padStart(2, "0");
  const dd = String(date.getDate()).padStart(2, "0");
  return calendar[`${mm}-${dd}`] ?? [];
}

// Names for each of the 7 days (Monday–Sunday) of ISO week `week` in ISO
// year `isoYear`.
export function nameDaysForWeek(isoYear, week, calendar = NAME_DAYS_FI) {
  const monday = mondayOf(week, isoYear);
  const days = [];
  for (let i = 0; i < 7; i++) {
    const date = new Date(monday);
    date.setDate(monday.getDate() + i);
    days.push({ date, names: nameDaysForDate(date, calendar) });
  }
  return days;
}
